#include "libft.h"
#include <unistd.h>

void	ft_puthex(unsigned int x)
{
	if (x > 15)
	{
		ft_puthex(x / 16);
	}
	if (x % 16 > 9)
	{
		x = x % 16 + 'a' - 10;
	}
	else
	{
		x = x % 16 + '0';
	}
	write (1, &x, 1);
}
